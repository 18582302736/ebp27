/**
 * 获取指定天的书写模板 HTML（内联 textarea 模式）
 * @param {number} day
 * @returns {string} HTML
 */
function getACTWorksheetHtml(day) {
  const parts = ACT_WORKSHEET_PARTS[day];
  if (!parts) return '';
  let html = '';
  // 书写指南
  if (parts.guideHtml) {
    html += '<div class="ws-guide">' + parts.guideHtml + '</div>';
  }
  // 书写模板
  if (parts.templateHtml) {
    html += '<div class="ws-template">' + parts.templateHtml + '</div>';
  }
  // 书写示例（折叠）
  if (parts.exampleHtml) {
    html += '<details class="ws-example"><summary>查看书写示例</summary>';
    html += '<div class="ws-example-body">' + parts.exampleHtml + '</div>';
    html += '</details>';
  }
  return html;
}